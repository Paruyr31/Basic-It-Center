import random
print("Surprise cake")
count = random.randint(1, 5)
while count != 0:
    if count == 1:
        print("This Apple cake")
        break
    elif count == 2:
        print("This Pier cake")
        break
    elif count == 3:
        print("This Lemon cake")
        break
    elif count == 4:
        print("This Orange cake")
        break
    else:
        print("This Chocolate cake")
        break

